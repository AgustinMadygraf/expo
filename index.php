  <h1>Proyecto de Monitoreo y Optimización - OEE%</h1>
  <p>El presente proyecto tiene como objetivo mejorar la eficiencia de producción y reducir el impacto ambiental en la industria de las bolsas de papel biodegradable. Se están utilizando tecnologías innovadoras, como el monitoreo de la materia prima y el producto final, el control de la velocidad de la máquina y la optimización del consumo energético.</p>
  <p>Mediante la implementación de balanzas precisas para medir la cantidad de materia prima y producto final, así como el uso de un Arduino y un encoder para monitorear y ajustar la velocidad de la máquina, buscamos lograr un uso más eficiente de los recursos y reducir los desperdicios innecesarios.</p>
  <p>Además, el análisis de las redes eléctricas nos permitirá identificar ineficiencias energéticas y tomar medidas para optimizar el consumo eléctrico. Todo esto contribuirá a mejorar el OEE% (Overall Equipment Efficiency), que es una métrica utilizada para evaluar la eficiencia global de la máquina de bolsas de papel biodegradable.</p>
  <p>El OEE% se calcula multiplicando tres factores.</p>
  <p>La disponibilidad, que mide el tiempo en que la máquina está en funcionamiento; </p>
  <p>El <a href="PRF/GET_SQL/">rendimiento</a>,
    que evalúa la producción real en comparación con la producción teórica;</p> 

  
  <p>La calidad, que considera la cantidad de productos defectuosos o de baja calidad.</p>
  <p>Este proyecto tiene un impacto positivo en el medio ambiente, promoviendo la reducción de desperdicios de materia prima, la eficiencia energética y el uso de materiales biodegradables en lugar de bolsas de plástico.</p>
  <p>¡Juntos podemos avanzar hacia una producción más sostenible y amigable con el medio ambiente!</p>
